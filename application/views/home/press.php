					<h1>Enverro signs HOA to aquire DateTix</h1>
				</p>
				<br />								
				<br />								
				<p>
				<b>ENVERRO SIGNS HEADS OF AGREEMENT TO ACQUIRE DATETIX LIMITED, A MOBILE AND CLOUD-BASED MARKETPLACE FOR REAL LIFE DATES</b>
				</p>								
				<br />								
				<p>Enverro Ltd ("Enverro") is pleased to announce that it has signed a binding Heads of Agreement with Datetix Limited ("DateTix") and
				Datetix shareholders to acquire Datetix ("Proposed Acquisition"). Founded in 2013 and based in Hong Kong, DateTix is a mobile and
				cloud-based marketplace for people to host, discover, and go on real life dates. The Proposed Acquisition will be conducted by way of
				the exercise of an option by Enverro to acquire all the shares on issue in DateTix, subject to the satisfaction of certain conditions.
				</p>								
				<br />								
				<p>The Proposed Acquisition has the support of the board of Enverro, who intend to recommend the Proposed Acquisition to its shareholders
				subject to it receiving the support of an independent expert. The directors of Enverro believe that the Proposed Acquisition will deliver
				value for shareholders by diversifying its existing portfolio of cloud-based software into consumer applications in addition to enterprise
				products and services.
				</p>
				<br />		
				<p><b>Proposed Acquisition summary</b></p>
				<br />								
				<p>If Enverro proceeds with the Proposed Acquisition, the shareholders of DateTix (or their nominees) will be issued with the following securities as consideration for all the issued shares in DateTix:</p>
				<p>&nbsp;</p>
				<p>
				<div class="cms-bullet-list">
				<ul>
				<li>12,500,000 fully paid ordinary shares at a deemed issue price of $0.40 per share;</li>																
				<li>4,000,000 class A performance rights that convert into fully paid ordinary shares in Enverro, on a one for one basis, in the event that within 24 months of settlement of the acquisition certain Performance Events are satisfied;</li>
				<li>4,000,000 class B performance rights that convert into fully paid ordinary shares in Enverro, on a one for one basis, in the event that within 24 months of settlement of the acquisition certain Performance Events are satisfied; and</li>
				<li>4,000,000 class C performance rights that convert into fully paid ordinary shares in Enverro, on a one for one basis, in the event that within 36 months certain Performance Events are satisfied.</li>
				</ul>
				</div>							
				</p>
				<p>&nbsp;</p>
				<p>The Performance Events which trigger the issue of Enverro ordinary shares are to be approved by the parties in consultation with ASX as soon as practicable after the date of this announment</p>
				<br/>	
				<p>Under the terms of the Heads of Agreement, after settlement Enverro will appoint up to three directors nominated by DateTix to the board of Enverro and grant the following options to employees and to a director to be appointed to the Board of Enverro:</p>
				<p>&nbsp;</p>
				<p>
				<div class="cms-bullet-list">
				<ul>
				<li>1,500,000 employee options with an exercise price of $0.40, vesting in four equal tranches of 25% each 12, 24, 36 and 48 months after the date of grant and expiring 31 December 2020;</li>
				<li>100,000 director options with an exercise price of $0.40 and expiring 31 December 2020.</li>
				</ul>
				</div>											
				</p>
				<p>&nbsp;</p>
				<p><b>Capital structure after completion of the proposed acquisition</b></p>
				<br />								
				<p>Enverro currently has 9,004,546 fully paid ordinary shares on issue which would increase to 21,504,546 upon completion of the Proposed Acquisition.
				The number of performance rights would increase from zero to 12,000,000 and the number of options on issue (with various exercise prices and conditions) would increase
				from 1,542,004 to 3,142,004. 
				</p>
				<br />								
				<p><b>Conditions precedent</b></p>
				<br />
				<p>Completion of the Proposed Acquisition is subject to the satisfaction or waiver of a number of conditions including:
				<div class="cms-bullet-list">
				<ul>
				<li>Enverro completing due diligence on Datetix;</li>
				<li>Enverro obtaining all necessary shareholder and regulatory approvals;</li>
				<li>the parties approving the terms, conditions and structure of the performance rights in consultation with ASX;</li>
				<li>to the extent required by ASX or the ASX Listing Rules, each Datetix shareholder entering into a restriction agreement in relation to the shares and performance rights issued to them; and</li>
				<li>any required ASX and shareholder approvals being obtained to allow the Proposed Acquisition to proceed.</li>
				</ul>
				</div>											
				</p>
				<p>&nbsp;</p>
				<p><b>Exclusivity</b></p>
				<br />								
				<p>Datetix and its shareholders are bound by an exclusivity period commencing on the date of the Heads of Agreement and ending on the earlier of (i) the date that is 3 months from the
				date of the Heads of Agreement (or such earlier date agreed in writing by the parties); and (ii) termination of the Heads of Agreement.</p>
				<br />								
				<p><b>Shareholder approval</b></p>
				<br />								
				<p>A notice of meeting seeking shareholder approval for the resolutions required to effect the Proposed Acquisition will be sent to Enverro shareholders in due course.
				An independent expert will be commissioned to prepare a report in relation to the Proposed Acquisition.</p>
